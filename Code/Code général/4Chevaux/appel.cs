        public void AttributionStart(int nbrJoueur, ref int[,] plateau)
        {
            if (nbrJoueur == 2)
            {
                for (int i = 0; i < 4; i++)
                {
                    plateau[0, i] = i + 1;
                }
                for (int i = 4; i < 8; i++)
                {
                    plateau[0, i] = i + 1;
                }
            }
            else if (nbrJoueur == 3)
            {
                for (int i = 0; i < 4; i++)
                {
                    plateau[0, i] = i + 1;
                }
                for (int i = 4; i < 8; i++)
                {
                    plateau[0, i] = i + 1;
                }
                for (int i = 8; i < 12; i++)
                {
                    plateau[0, i] = i + 1;
                }
            }
            else
            {
                for (int i = 0; i < 4; i++)
                {
                    plateau[0, i] = i + 1;
                }
                for (int i = 4; i < 8; i++)
                {
                    plateau[0, i] = i + 1;
                }
                for (int i = 8; i < 12; i++)
                {
                    plateau[0, i] = i + 1;
                }
                for (int i = 12; i < 16; i++)
                {
                    plateau[0, i] = i + 1;
                }
            }
        }



        public void ConvertirPlateauEnString(int nbrJoueur, int[,] plateau, out string plateauVisuel)
        {
            bool trouver;
            int j;
            plateauVisuel = "";
            for (int i = 0;i < 26; i++)
            {
                plateauVisuel = plateauVisuel + "+";
                plateauVisuel = plateauVisuel + "-";
            }
            plateauVisuel = plateauVisuel + "+\n|";
            for (int i = 0; i < nbrJoueur + 1; i++)
            {
                trouver = false;
                j = 0;
                do
                { 
                    if (plateau[i, j] == i)
                    {
                        for (int k = 1; k < j - 1; k++)
                        {
                            plateauVisuel = plateauVisuel + " ";
                            plateauVisuel = plateauVisuel + "|";
                        }
                        plateauVisuel = plateauVisuel + "H";
                        for (int k = 0; k < 27 - j; k++)
                        {
                            plateauVisuel = plateauVisuel + " ";
                            plateauVisuel = plateauVisuel + "|";
                        }
                        trouver = true;
                    }
                    j++;
                } while (trouver == false);
                plateauVisuel = plateauVisuel + "+\n|";
            }
        }

        
